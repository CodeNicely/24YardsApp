public class SubCategoryData {

    String title;
    String location;
    String price;
    String image;

    public SubCategoryData(String title, String location, String price, String image) {
        this.title = title;
        this.location = location;
        this.price = price;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public String getLocation() {
        return location;
    }

    public String getPrice() {
        return price;
    }

    public String getImage() {
        return image;
    }
}
